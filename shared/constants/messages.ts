/** 한글 주격 조사 — 받침 있으면 '이', 없으면 '가' (비한글 종결은 '가' 폴백). */
export const subjectParticle = (word: string): '이' | '가' => {
  const code = word.charCodeAt(word.length - 1);
  if (code < 0xac00 || code > 0xd7a3) return '가';
  return (code - 0xac00) % 28 > 0 ? '이' : '가';
};


type Params = { slug: string };

export function generateStaticParams(): Params[] {
  return Object.keys(DOCS).map((slug) => ({ slug }));
}

export async function generateMetadata({
  params,
}: {
  params: Promise<Params>;
}): Promise<Metadata> {
  const { slug } = await params;
  const doc = DOCS[slug];
  if (!doc) return {};
  return {
    title: doc.title,
    description: doc.description,
  };
}

export default async function LegalPage({
  params,
}: {
  params: Promise<Params>;
}) {
  const { slug } = await params;
  const doc = DOCS[slug];
  if (!doc) notFound();

  return (
    <>
      <PageHeader
        eyebrow="약관 · 정책"
        title={doc.title}
        description={doc.description}
      />

      <section className="section !pt-0">
        <div className="container-site">
          <div className="mx-auto max-w-3xl space-y-5">
            {doc.sections.map((s) => (
              <div key={s.h} className="glass-card p-7">
                <h2 className="text-lg font-bold text-rink-900">{s.h}</h2>
                {s.p && (
                  <p className="mt-3 text-sm leading-relaxed text-wtext-2">
                    {s.p}
                  </p>
                )}
                {s.items && (
                  <ul className="mt-3 list-disc space-y-1.5 pl-5 text-sm leading-relaxed text-wtext-2 marker:text-ice-500">
                    {s.items.map((item, i) => (
                      <li key={i}>{item}</li>
                    ))}
                  </ul>
                )}
              </div>
            ))}
            <p className="mt-8 text-center text-xs text-wtext-4">
              {doc.effective}
            </p>
          </div>
        </div>
      </section>
    </>
  );
}
